    static void FindAverageWordLength(string text)
    {
        int count = 0;
        double sum = 0;
        string currentWord = string.Empty;

        for (int i = 0; i < text.Length; i++)
        {
            if (char.IsLetter(text[i]))
            {
                currentWord += text[i];
            }
            else
            {
                if (currentWord.Length >= 3)
                {
                    count++;
                    sum += currentWord.Length;
                }

                currentWord = string.Empty;
            }
        }

        if (count > 0)
        {
            double averageLength = sum / count;
            string formattedAverageLength = averageLength.ToString("f2");

            Console.WriteLine($"Words count: {formattedAverageLength}");
        }
    }

    static void Find5MostRepeatedWords(string text)
    {
        Dictionary<string, int> words = new Dictionary<string, int>();
        string currentWord = string.Empty;

        for (int i = 0; i < text.Length; i++)
        {
            if (char.IsLetter(text[i]))
            {
                currentWord += text[i];
            }
            else
            {
                if (currentWord.Length >= 3)
                {
                    if (words.ContainsKey(currentWord))
                    {
                        words[currentWord]++;
                    }
                    else
                    {
                        words.Add(currentWord, 1);
                    }
                }

                currentWord = string.Empty;
            }
        }

        Dictionary<string, int> top5Words = words
            .OrderByDescending(w => w.Value)
            .ThenBy(w => w.Key)
            .Take(5)
            .ToDictionary();
        
        foreach (var pair in top5Words)
        {
            Console.WriteLine($"{pair.Key}: {pair.Value} times");
        }
    }

