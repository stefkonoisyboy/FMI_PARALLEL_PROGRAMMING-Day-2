    static void Benchmark(Action action, string name)
    {
        Stopwatch stopwatch = new Stopwatch();
        stopwatch.Start();
        action();
        stopwatch.Stop();
        Console.WriteLine($"{name}: {stopwatch.ElapsedMilliseconds} ms.");
    }

    static void ProcessLinear(string text)
    {
        FindAllWordsCount(text);
        FindShortestWord(text);
        FindLongestWord(text);
        FindAverageWordLength(text);
        Find5MostRepeatedWords(text);
        Find5LeastRepeatedWords(text);
    }

    static void ProcessParallel(string text)
    {
        Thread[] threads = new Thread[6];
        
        threads[0] = new Thread(() => FindAllWordsCount(text));
        threads[0].Start();
        
        threads[1] = new Thread(() => FindShortestWord(text));
        threads[1].Start();
        
        threads[2] = new Thread(() => FindLongestWord(text));
        threads[2].Start();
        
        threads[3] = new Thread(() => FindAverageWordLength(text));
        threads[3].Start();
        
        threads[4] = new Thread(() => Find5MostRepeatedWords(text));
        threads[4].Start();
        
        threads[5] = new Thread(() => Find5LeastRepeatedWords(text));
        threads[5].Start();

        for (int i = 0; i < threads.Length; i++)
        {
            threads[i].Join();
        }
    }

    static void FindAllWordsCount(string text)
    {
        int count = 0;
        string currentWord = string.Empty;

        for (int i = 0; i < text.Length; i++)
        {
            if (char.IsLetter(text[i]))
            {
                currentWord += text[i];
            }
            else
            {
                if (currentWord.Length >= 3)
                {
                    count++;
                }

                currentWord = string.Empty;
            }
        }

        Console.WriteLine($"Words count: {count}");
    }

    static void FindShortestWord(string text)
    {
        string shortestWord = string.Empty;
        string currentWord = string.Empty;

        for (int i = 0; i < text.Length; i++)
        {
            if (char.IsLetter(text[i]))
            {
                currentWord += text[i];
            }
            else
            {
                if (currentWord.Length >= 3 && (string.IsNullOrEmpty(shortestWord) || currentWord.Length < shortestWord.Length))
                {
                    shortestWord = currentWord;
                }

                currentWord = string.Empty;
            }
        }

        Console.WriteLine($"Shortest word: {shortestWord}");
    }
    
